uint16_t sml_crc16(const unsigned char *cp, size_t len)
{
	uint16_t fcs = 0xFFFF;
	while (len--) {
		fcs = (fcs >> 8) ^ crctab[(fcs ^ *cp++) & 0xff];
	}
	fcs ^= 0xffff;
	return fcs;
}

int sml_crc16_check(const unsigned char *sml_file, size_t len)
{
	uint16_t crc = sml_crc16(sml_file, len-2);
	if (*(uint16_t*)(sml_file+len-2) == crc) {
		return 1; // crc is valid
	} else {
		return 0; // crc is invalid
	}
}
void serial_port_set_9600_8N1(int fd)
{
	// 9600 8N1
	struct termios options;
	tcgetattr(fd, &options);
	cfsetispeed(&options, B9600);
	cfsetospeed(&options, B9600);
	options.c_cflag |= (CLOCAL | CREAD);
	options.c_cflag &= ~PARENB;
	options.c_cflag &= ~CSTOPB;
	options.c_cflag &= ~CSIZE;
	options.c_cflag |= CS8;
	options.c_cflag &= ~CRTSCTS;
	cfmakeraw(&options);
	tcsetattr(fd, TCSANOW, &options);
}

int q3c_init(const char *ir_device)
{
	int ir = open(ir_device, O_RDWR | O_NOCTTY | /*O_NONBLOCK |*/ O_SYNC);
	if (ir == -1) {
		perror("Unable to open ir device");
		return -1;
	}

	serial_port_set_9600_8N1(ir);
	return ir;
}

void q3c_cleanup(int ir)
{
	close(ir);
}

float q3c_read(int ir)
{
	int n = 0;
	unsigned char response[1024];

	while(n < Q3C_RESPONSE_LENGTH) {
		n = write(ir, sml_request, sizeof(sml_request));
		//fprintf(stderr, "Sent %d bytes\n", n);

		n = 0;
		while (n < Q3C_RESPONSE_LENGTH) {
			fd_set irset;
			FD_ZERO(&irset);
			FD_SET(ir, &irset);
			struct timeval timeout;
			timeout.tv_sec = 0;
			timeout.tv_usec = 100000;
			int rv = select(ir+1, &irset, NULL, NULL, &timeout);
			if (rv == -1) {
				perror("select"); // an error occured
			} else if (rv == 0) {
				fprintf(stderr, "Timeout while reading from IR\n\n");
				break; // timeout
			} else {
				int i = read(ir, response+n, sizeof(response)-n);
				if (n > 0) {
					//fprintf(stderr, "[1A\r");
				}
				n += i;
				//fprintf(stderr, "Recv %d bytes\n", n);
			}
		}
		if (n >= Q3C_RESPONSE_LENGTH) {
			// enough data received
			if (sml_crc16_check(sml_request, sizeof(sml_request)) == 0) {
				// invalid crc
				fprintf(stderr, "Invalid CRC\n\n");
				n = 0;
			}
		}
	}

	unsigned char *t0pos = memmem(response, n, sml_t0_token, sizeof(sml_t0_token));

	if (t0pos == NULL || (int)(t0pos-response)+28 > n) {
		return -1;
	}

	float t0_value = ntohl(*(uint32_t*)(t0pos+24));
	t0_value /= 100;

	return t0_value;
}
